package com.rogers.yaml.pojo;

public class CHOneViewConfig {
	
	private String url;
	private String browser;
	private String language;
	private String loginID;
	private String usrID;
	private String oneViewenv;

	/**
	 * @return the language
	 */
	public String getLanguage() {
		return language;
	}
	/**
	 * @param language the language to set
	 */
	public void setLanguage(String language) {
		this.language = language;
	}
	/**
	 * @return the browser
	 */
	public String getBrowser() {
		return browser;
	}
	/**
	 * @param browser the browser to set
	 */
	public void setBrowser(String browser) {
		this.browser = browser;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param rogersURL the rogersURL to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	
	/**
	 * @return the loginID
	 */
	public String getLoginID() {
		return loginID;
	}
	/**
	 * @param loginID the loginID to set
	 */
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	
	/**
	 * @return the oneViewenv
	 */
	public String getOneViewenv() {
		return oneViewenv;
	}
	/**
	 * @param oneViewenv the oneViewenv to set
	 */
	public void setOneViewenv(String oneViewenv) {
		this.oneViewenv = oneViewenv;
	}
	
	/**
	 * @return the usrID
	 */
	public String getUsrID() {
		return usrID;
	}
	/**
	 * @param usrID the usrID to set
	 */
	public void setUsrID(String usrID) {
		this.usrID = usrID;
	}
}
