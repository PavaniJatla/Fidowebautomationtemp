package com.rogers.yaml.pojo;

public class LoginDetails {
	

	private String loginID;
	private String usrID;

	private String loginIDFieldScience;
	private String usrIDFieldScience;


	public String getLoginID() {
		return loginID;
	}

	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}

	public String getUsrID() {
		return usrID;
	}

	public void setUsrID(String usrID) {
		this.usrID = usrID;
	}

	public String getLoginIDFieldScience() {
		return loginIDFieldScience;
	}

	public void setLoginIDFieldScience(String loginIDFieldScience) {
		this.loginIDFieldScience = loginIDFieldScience;
	}

	public String getUsrIDFieldScience() {
		return usrIDFieldScience;
	}

	public void setUsrIDFieldScience(String usrIDFieldScience) {
		this.usrIDFieldScience = usrIDFieldScience;
	}

}
