		getRogersOVChoosePhonePage().clickContinueButton();
		//---------------------------------------------Plan config page--------------------------------------------------
		getRogersOVPlanConfigPage().clkRadioButtonNoTerm();
		getRogersOVPlanConfigPage().clkPreCartDeviceCostContinueButtonForNac();
