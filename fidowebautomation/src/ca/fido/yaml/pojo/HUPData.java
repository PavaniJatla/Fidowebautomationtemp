package ca.fido.yaml.pojo;

public class HUPData {
	
	private String newDevice;
	private String username;
	private String password;
	private String ctn;
	/**
	 * @return the newDevice
	 */
	public String getNewDevice() {
		return newDevice;
	}
	/**
	 * @param newDevice the newDevice to set
	 */
	public void setNewDevice(String newDevice) {
		this.newDevice = newDevice;
	}
	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the ctn
	 */
	public String getCtn() {
		return ctn;
	}
	/**
	 * @param ctn the ctn to set
	 */
	public void setCtn(String ctn) {
		this.ctn = ctn;
	}

}
